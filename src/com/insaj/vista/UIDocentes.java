    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
       try {
                int row = tablaDocentes.getSelectedRow();
                if(row==-1){
                   JOptionPane.showMessageDialog(null,"Seleccione un registro de la tabla","Alerta",JOptionPane.WARNING_MESSAGE);
                }else{
                    String tableClick = (tablaDocentes.getModel().getValueAt(row, 0)).toString();
                    control.Borrar(tableClick);
                    MostrarDatos(control.executeHQLQuery("From Docentes"));//actuliza la tabla
                }
             } catch (Exception ex) {
               JOptionPane.showMessageDialog(null, ex);
             }
    }//GEN-LAST:event_btnEliminarActionPerformed

         private void MostrarDatos(List resultList) {
        Vector<String> tableHeaders = new Vector<String>();
        Vector tableData = new Vector();
        tableHeaders.add("ID");
        tableHeaders.add("NOMBRES");
        tableHeaders.add("PELLIDOS");
        tableHeaders.add("CONTRATO");
        tableHeaders.add("HORAS-PLANTA");
        tableHeaders.add("HORAS-CLASES");
        
        for (Object o : resultList) {
            Docentes docen = (Docentes) o;
            Vector<Object> oneRow = new Vector<Object>();
            oneRow.add(docen.getIdDocente());
            oneRow.add(docen.getNombres());
            oneRow.add(docen.getApellidos());
            oneRow.add(docen.getContrato());
            oneRow.add(docen.getNumHorasPlanta());
            oneRow.add(docen.getNumHorasClases());
            tableData.add(oneRow);
        }
        tablaDocentes.setModel(new DefaultTableModel(tableData, tableHeaders));
    }
