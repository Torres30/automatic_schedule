    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        
       if(tablaDocentes.getSelectedRow()!=-1){//si hay un registro seleccionado pasa   
            DialogoDocentes diag = new DialogoDocentes();
            int row = tablaDocentes.getSelectedRow();//captura la fila seleccionadad
            String tableID = (tablaDocentes.getModel().getValueAt(row, 0)).toString();//guarda el id de la columna seleccionada
             try{
                 diag.txtIdDocentes.setText(tableID);//muestra el id en el textfield
                     Docentes docen = control.getDocentesByID(tableID);
                     //mostarando los datos en texfields
                      diag.txtNombre.setText(docen.getNombres());
                      diag.txtApellido.setText(docen.getApellidos());
                      diag.txtContrato.setText(docen.getContrato());
                      diag.txtNuHorasPlanta.setText(docen.getNumHorasPlanta().toString());
                      diag.txtNuHorasClases.setText(docen.getNumHorasClases().toString());
                      if(docen.getR()!=null && docen.getG() != null && docen.getB() != null){
                        diag.jPanel1.setBackground(new Color(docen.getR(), docen.getG(), docen.getB()));
                      }
                      List<Materias> lis = new ArrayList<>(docen.getMaterias());
                      diag.setListaMates(lis);
                      diag.MostrarDatos(lis);
                      diag.addWindowListener(new WindowAdapter() {
                             @Override
                             public void windowClosed(WindowEvent e)  {
                                 // throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
                                 MostrarDatos(control.executeHQLQuery("From Docentes"));
                             }
                     });
                     dialogos.MostrarNuevoDialogo(diag, "Editar Docentes");
                 
             }catch(HibernateException he){
                 //mostrar mensaje error
             }
    }else{
            JOptionPane.showMessageDialog(null,"Seleccione un registro de la tabla","Alerta",JOptionPane.WARNING_MESSAGE);
       }
    }//GEN-LAST:event_btnEditarActionPerformed

