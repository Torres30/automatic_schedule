    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        
       if(tablaSecciones.getSelectedRow()!=-1){//si hay un registro seleccionado pasa   
            DialogoSecciones diag = new DialogoSecciones();
             diag.setAlwaysOnTop(true);
            int row = tablaSecciones.getSelectedRow();//captura la fila seleccionadad
            String tableID = (tablaSecciones.getModel().getValueAt(row, 0)).toString();//guarda el id de la columna seleccionada
             try{
                 diag.txtIdSeccion.setText(tableID);//muestra el id en el textfield
                     Secciones secci = control.getSeccionesByID(tableID);
                     //mostarando los datos en texfields
                      diag.txtEespecialidad.setText(secci.getEspecialidad());
                      diag.txtAnio.setText(secci.getAnio());
                      diag.txtSeccion.setText(secci.getSeccion());
                      List<Materias> lis = new ArrayList<>(secci.getMateriases());
                      diag.setListaMates(lis);
                      diag.MostrarDatos(lis);
                      diag.addWindowListener(new WindowAdapter() {
                             @Override
                             public void windowClosed(WindowEvent e)  {
                                 // throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
                                 MostrarDatos(control.executeHQLQuery("from Secciones"));
                             }
                     });
                     dialogos.MostrarNuevoDialogo(diag, "Editar Secciones");
                 
             }catch(HibernateException he){
                 //mostrar mensaje error
             }
    }else{
            JOptionPane.showMessageDialog(null,"Seleccione un registro de la tabla","Alerta",JOptionPane.WARNING_MESSAGE);
       }
    }//GEN-LAST:event_btnEditarActionPerformed

