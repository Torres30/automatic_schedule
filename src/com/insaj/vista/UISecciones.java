    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
          try {
                int row = tablaSecciones.getSelectedRow();
                if(row==-1){
                   JOptionPane.showMessageDialog(null,"Seleccione un registro de la tabla","Alerta",JOptionPane.WARNING_MESSAGE);
                }else{
                    String tableClick = (tablaSecciones.getModel().getValueAt(row, 0)).toString();
                    control.Borrar(tableClick);
                    MostrarDatos(control.executeHQLQuery("From Secciones"));//actuliza la tabla
                }
             } catch (Exception ex) {
               JOptionPane.showMessageDialog(null, ex);
             }
    }//GEN-LAST:event_btnEliminarActionPerformed

         private void MostrarDatos(List resultList) {
        Vector<String> tableHeaders = new Vector<String>();
        Vector tableData = new Vector();
        tableHeaders.add("ID");
        tableHeaders.add("ESPECIALIDAD");
        tableHeaders.add("AÑO");
        tableHeaders.add("SECCION");

        for (Object o : resultList) {
            Secciones secci = (Secciones) o;
            Vector<Object> oneRow = new Vector<Object>();
            oneRow.add(secci.getIdSeccion());
            oneRow.add(secci.getEspecialidad());
            oneRow.add(secci.getAnio());
            oneRow.add(secci.getSeccion());
            tableData.add(oneRow);
        }
        tablaSecciones.setModel(new DefaultTableModel(tableData, tableHeaders));
    }
